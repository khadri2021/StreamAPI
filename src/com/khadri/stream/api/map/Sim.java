package com.khadri.stream.api.map;

public class Sim {

	private String number;

	private String operator;

	public Sim(String number, String operator) {
		super();
		this.number = number;
		this.operator = operator;
	}

	public String getNumber() {
		return number;
	}

	public String getOperator() {
		return operator;
	}

}
